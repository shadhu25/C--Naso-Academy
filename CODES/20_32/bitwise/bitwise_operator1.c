#include<stdio.h>
int main()
{
    int a=1,b=2;//a = 1(0000 0001), b = 2(0000 00010) 
    printf("a&b=%d\n",a&b);//1&2 = 0(0000 0000)
    printf("a&&b=%d\n",a&&b);//1&&2 = true && true = true = 1
    printf("a|b=%d\n",a|b);//1|2 = 3(0000 0011) 
    printf("a||b=%d\n",a||b);//1||2 = true && true = true = 1
    printf("~a=%d\n",~a);//here is 32bit 1=(0000000000000000000000000000001)  ~1=(11111111111111111111111111111110)=-2
    printf("a<<1 = %d\n",a<<1);// a<<1=(00000000000000000000000000000010)=2
    printf("a>>1 = %d\n",a>>1);// a>>1=(00000000000000000000000000000000)=0
    printf("a^b = %d\n",a^b);// a^b=(00000000000000000000000000000011)=3
    return 0;
}